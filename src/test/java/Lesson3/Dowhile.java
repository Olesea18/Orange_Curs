package Lesson3;


public class Dowhile {
    public static void main(String[] args) {
        int i=10;
        do{
            i++;
            System.out.println(i);
        }
        while (i<15);

        int a = 12;
        do {
            a++;
            System.out.println(a);
        }
        while (a<14);

        int b=10;
        do{
            b--;
            System.out.println(b);
        }
        while(b>1);
    }
}


